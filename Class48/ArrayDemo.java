package Class48;

public class ArrayDemo {
    public static void main(String[] args) {
        int[] arr = new int[5];

        arr[0] = 99;
        arr[1] = 98;
        arr[2] = 97;
        arr[3] = 96;
        arr[4] = 95;
        System.out.println("________________________________");
        System.out.println(arr[0] + "|" + arr[1] + "|" + arr[2] + "|" + arr[3] + "|" + arr[4] + "|");
        System.out.println("--------------------------------");
    }
}
